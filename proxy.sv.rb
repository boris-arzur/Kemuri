class Proxy
  def execute request
    url = URI.parse('http://' + request[2..-1]*'/')
    log url
    req = Net::HTTP::Get.new(url.path)
    res = Net::HTTP.start(url.host, url.port) {|http|
      http.request(req)
    }
    res.body.force_encoding('UTF-8') + Static::search
  end
end
